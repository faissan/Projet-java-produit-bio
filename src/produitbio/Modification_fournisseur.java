    private void enregistrerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_enregistrerMouseClicked
        Connection co = null;
        Statement st = null;
        ResultSet rs = null;
        ResultSet rs1 = null;        
        //Les variables
        String v_ref = ref_fournisseur.getText();
        String v_name = name_fournisseur.getText();
        String v_adr = adr_fournisseur.getText();
        String v_tel = tel_fournisseur.getText();      
        if (v_name.equals("")||v_adr.equals("")||v_tel.equals("")||v_ref.equals("")){
            message_error.setForeground(Color.red);
            message_error.setText("Veuillez remplir tous les champs !!!");
        }
        else
        {
            if(JOptionPane.showConfirmDialog(null, "Confirmer mofification?", "CONFIRMATION",JOptionPane.YES_NO_OPTION) == JOptionPane.YES_OPTION){
                try {
                    Class.forName("com.mysql.jdbc.Driver").newInstance();
                    co = DriverManager.getConnection("jdbc:mysql://localhost/biomarket?characterEncoding=latin1","root","");
                    st = co.createStatement();
                    int ref =  Integer.parseInt(v_ref);
                    st.executeUpdate("UPDATE fournisseur SET nom_fournisseur='"+v_name+"', adresse_fournisseur='"+v_adr+"', telephone_fournisseur='"+v_tel+"' WHERE reference_fournisseur ="+ref+"");
                    //Réinitialisation des champs
                    message_error.setForeground(Color.GREEN);
                    message_error.setText("Données bien enregistrées"); 
                    ref_fournisseur.setText("");
                    name_fournisseur.setText("");
                    adr_fournisseur.setText("");
                    tel_fournisseur.setText("");
                } catch (Exception e) {
                    message_error.setText("Erreur");
                }
            }
            else
            {
                message_error.setText("Données non ajoutées");
                 
            }
        }
    }//GEN-LAST:event_enregistrerMouseClicked

